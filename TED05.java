public class TED05 {
  
    public static void main(String args[]) {
      int cont = 1;
      while (cont <= 20){
          
          if (cont%5 == 0){
            System.out.print(cont + "-");
            System.out.println('\t');
          }
          else{
            System.out.print(cont + "-");
            
          }
         cont++;
      }
      
    }
}
